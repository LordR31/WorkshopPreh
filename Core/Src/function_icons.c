int Icons_start_screen(int *timer) {
	for (int i = 0; i < 3; i++) {

		if (i == 0) {
			*timer = 0;
			st7565_drawbitmap(buffer, 0, 0, starter_1, 128, 64, 1);
			st7565_write_buffer(buffer);
			while (*timer < 500)
				continue;
			Icons_clear_screen();

		}

		*timer = 0;
		st7565_drawbitmap(buffer, 0, 0, starter_2, 128, 64, 1);
		st7565_write_buffer(buffer);
		while (*timer < 500)
			continue;
		Icons_clear_screen();

		*timer = 0;
		st7565_drawbitmap(buffer, 0, 0, starter_3, 128, 64, 1);
		st7565_write_buffer(buffer);
		while (*timer < 500)
			continue;
		Icons_clear_screen();

		*timer = 0;
		st7565_drawbitmap(buffer, 0, 0, starter_2, 128, 64, 1);
		st7565_write_buffer(buffer);
		while (*timer < 500)
			continue;
		Icons_clear_screen();

		*timer = 0;
		st7565_drawbitmap(buffer, 0, 0, starter_1, 128, 64, 1);
		st7565_write_buffer(buffer);
		while (*timer < 500)
			continue;
		Icons_clear_screen();
	}
	return 0;
}

